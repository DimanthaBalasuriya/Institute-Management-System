    private void loadProgressBar() {
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i < 101; i++) {
                    jProgressBar1.setValue(i);
                    try {
                        Thread.sleep(100);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                    if (i == 100) {
                        Login log = new Login();
                        StudentAttendanceCollect sac = new StudentAttendanceCollect();
                        log.setVisible(true);
                        sac.setVisible(true);
                        dispose();
                    }

                }
            }
        });
        t.start();
    }
    Date d;
    SimpleDateFormat sdf;
